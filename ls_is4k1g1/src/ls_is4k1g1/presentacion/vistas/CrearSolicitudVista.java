    @Override
    public void setDetalle(ArrayList<LineaSolicitud> detalle) {
        
        DefaultTableModel model = (DefaultTableModel) tbDetalle.getModel();
        model.setNumRows(0);
        Object rowData[] = new Object[4];
        for(LineaSolicitud linea: detalle){
            rowData[0] = linea.getPrenda().toString();
            rowData[1] = linea.getTalle().toString();
            rowData[2] = linea.getColor().toString();
            rowData[3] = linea.getCantidad();
            model.addRow(rowData);
        }
    }

    private void agregarLineaSolicitud() {
        presentador.agregarLineaSolicitud((AbcPrenda)cbPrenda.getSelectedItem(),
                (AbcTalle)cbTalle.getSelectedItem(),
                (AbcColor)cbColor.getSelectedItem(),
                Integer.parseInt(txtCantidad.getText()));
    }

    private void confirmarSolicitud() {
        presentador.confirmarSolicitud((AbcSucursal)cbSucursal.getSelectedItem());
    }

    @Override
    public int getCantidad() {
        return Integer.parseInt(txtCantidad.getText());
    }
